            'description' => 'required',
            'start_date' => 'required',
            'end_date' => 'required',
            'location' => 'required',
            'visitors' => 'required',


        ]);

        Conference::create($request->all());

        return redirect()->route('conferences.index')
            ->with('success', 'Conference created successfully.');
